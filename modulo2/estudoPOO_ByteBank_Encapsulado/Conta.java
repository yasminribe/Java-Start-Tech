package estudoPOO_ByteBank_Encapsulado;


public class Conta {
    private double saldo;
    private int agencia;
    private int numero;
    private Cliente titular;

    private static int total; // quando coloca static no atributo, passa pertencer a classe não ao objeto

    public Conta(int agencia, int numero) {
        total++;
        System.out.println("o total de contas criadas: " + total);
        this.agencia = agencia;
        this.numero = numero;
    }


    //sem retorno
    public void deposita(double valor) {
        this.saldo += valor;
    }

    public boolean saca(double valor) {
        if (this.saldo >= valor) {
            this.saldo -= valor;
            return true;
        }
        return false;
    }

    public boolean transfere(double valor, Conta destino) {
        if (this.saldo >= valor) {
            saca(valor);
            destino.deposita(valor);
            return true;
        }
        return false;
    }

    public double getSaldo() {
        return this.saldo;
    }

    public int getNumero() {
        return this.numero;
    }

    public void setNumero(int numero) {
        if (numero <= 0) {
            System.out.println("Erro, número da conta invalido");
            return;
        }
        this.numero = numero;
    }

    public int getAgencia() {
        return this.agencia;
    }

    public void setAgencia(int agencia) {
        if (agencia <= 0) {
            System.out.println("Error, agencia invalida");
            return;
        }
        this.agencia = agencia;
    }

    public Cliente getTitular() {
        return this.titular;
    }

    public void setTitular(Cliente titular) {
        this.titular = titular;
    }

    public static int getTotal() {
        return Conta.total;
    }
}

